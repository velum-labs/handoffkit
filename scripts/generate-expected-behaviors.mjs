import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const CHECK = process.argv.includes("--check");
const sourcePath = resolve("spec/testing/expected-behaviors.json");
const outputPath = resolve("docs/generated/expected-behaviors.md");
const contract = JSON.parse(readFileSync(sourcePath, "utf8"));

const CATEGORY_ORDER = [
  "model-exposure",
  "request-fidelity",
  "fusion",
  "reasoning",
  "streaming",
  "tools",
  "harness",
  "policy",
  "security",
  "sessions",
  "lifecycle",
  "runs",
  "process",
  "cli",
  "observability",
  "platform"
];

function cell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}

const lines = [
  "<!-- Generated by scripts/generate-expected-behaviors.mjs; do not edit. -->",
  "",
  "# FusionKit expected behavior contract",
  "",
  `Source: \`spec/testing/expected-behaviors.json\` (${contract.version}).`,
  "",
  "This is the reviewable list of product behaviors FusionKit claims. Required",
  "rows name the automated test and source anchor that prove the behavior;",
  "environment-gated rows name the reason and exact live command.",
  "",
  "## Axes",
  "",
  `- Providers: ${contract.axes.providers.map((value) => `\`${value}\``).join(", ")}`,
  `- Gateway doors: ${contract.axes.doors.map((value) => `\`${value}\``).join(", ")}`,
  `- Coding tools: ${contract.axes.tools.map((value) => `\`${value}\``).join(", ")}`,
  `- Fusion modes: ${contract.axes.fusionModes.map((value) => `\`${value}\``).join(", ")}`,
  `- Panel depth: ${contract.axes.panelDepths.map((value) => `\`${value}\``).join(", ")}`,
  ""
];

for (const category of CATEGORY_ORDER) {
  const behaviors = contract.behaviors.filter((behavior) => behavior.category === category);
  if (behaviors.length === 0) continue;
  lines.push(`## ${category}`, "", "| ID | Expected behavior | Status | Evidence |", "|---|---|---|---|");
  for (const behavior of behaviors) {
    const evidence =
      behavior.status === "required"
        ? `\`${behavior.testFile}\` — \`${behavior.anchor}\``
        : `${behavior.gateReason} Run: \`${behavior.liveCommand}\``;
    lines.push(
      `| \`${cell(behavior.id)}\` | ${cell(behavior.expectation)} | ${cell(behavior.status)} | ${cell(evidence)} |`
    );
  }
  lines.push("");
}

const rendered = `${lines.join("\n")}\n`;
if (CHECK) {
  if (!existsSync(outputPath) || readFileSync(outputPath, "utf8") !== rendered) {
    console.error("docs/generated/expected-behaviors.md is stale; run pnpm docs:generate-behaviors");
    process.exit(1);
  }
  console.log("expected behavior documentation check passed");
} else {
  writeFileSync(outputPath, rendered);
  console.log("generated docs/generated/expected-behaviors.md");
}
